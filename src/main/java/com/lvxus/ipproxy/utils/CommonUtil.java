    public static String getConnectionHtmlResultOnlyUrl(String urlString){
        return getConnectionHtmlResult(null,null,urlString,null);
    }
    public static String getConnectionHtmlResult(String host, Integer port, String urlString, String encoding) {
        String str = "";
        try {
            Proxy proxy = null;
            if (!StringUtils.isEmpty(host)&&!StringUtils.isEmpty(port)){
                InetSocketAddress inetSocketAddress = new InetSocketAddress(host, port);
                proxy = new Proxy(Proxy.Type.SOCKS,inetSocketAddress);
            }
            URL url = new URL(urlString);
            URLConnection urlConnection = proxy == null?url.openConnection():url.openConnection(proxy);
            HttpURLConnection httpURLConnection = (HttpURLConnection) urlConnection;
            httpURLConnection.setRequestProperty("User-Agent",getRandomUserAgent());
            httpURLConnection.setConnectTimeout(5000);
            httpURLConnection.connect();
            int responseCode = httpURLConnection.getResponseCode();
            if (HttpURLConnection.HTTP_OK != responseCode) {
                System.err.println("fail to connect!!!");
                return str;
            }
            System.out.println("begin connecting!!!");
            InputStream inputStream = httpURLConnection.getInputStream();
            InputStreamReader inputStreamReader = new InputStreamReader(inputStream);
            BufferedReader bufferedReader = new BufferedReader(inputStreamReader);
            String strLine;
            while (( strLine = bufferedReader.readLine())!= null){
                str +=strLine;
            }
            if (!StringUtils.isEmpty(encoding)){
                str = new String(str.getBytes(encoding));
            }
            bufferedReader.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return str;
    }

    public static List<String> getRegexHtmlResults(String regex, String str) {
        Pattern compile = Pattern.compile(regex);
        Matcher matcher = compile.matcher(str);
        List<String> resultList = new ArrayList<>();
        while (matcher.find()){
            resultList.add(matcher.group(1));
        }
        return resultList;
    }

    public static boolean regexIp(String ip) {
        Pattern compile = Pattern.compile(IpRegex);
        Matcher matcher = compile.matcher(ip);
        return matcher.matches();
    }
